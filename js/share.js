let twitter = document.getElementById("twitter");
twitter.addEventListener("click", () => {
  window.open(
    "https://twitter.com/share?text=https://pinoylucky.com/",
    "_blank"
  );
});
let FB = document.getElementById("FB");
FB.addEventListener("click", () => {
  window.open(
    "https://www.facebook.com/sharer.php?u=https://pinoylucky.com/",
    "_blank"
  );
});
